int		ft_count_if(char **tab, int (*f)(char*))
{
	int nb;
	int a;

	a = 0;
	nb = 0;
	while (tab[a])
	{
		if (f(tab[a]) == 1)
			nb++;
		a++;
	}
	return (nb);
}
