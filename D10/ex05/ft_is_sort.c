int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int a;

	a = 0;
	while (a < length - 2)
	{
		nb = f(tab[a], tab[a + 1]);
		if (f(tab[a + 1], tab[a + 2]) > nb)
			return (0);
		a++;
	}
	return (1);
}
