int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int a;

	a = 0;
	if (n <= 0)
		return (0);
	n--;
	while ((s1[a] || s2[a]) && (n--))
	{
		if (s1[a] != s2[a])
			return (s1[a] - s2[a]);
		a++;
	}
	return (s1[a] - s2[a]);
}
