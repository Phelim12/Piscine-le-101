int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	if (ft_strlen(dest) < n)
		return (NULL);
	while (n--)
		dest[n] = src[n];
	return (dest);
}
