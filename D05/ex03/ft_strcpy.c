int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int a;

	a = -1;
	while (src[a++])
		dest[a] = src[a];
	return (src);
}
